package codeup;

import java.util.Scanner;

public class CodeUp1031 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int num = sc.nextInt();
        System.out.printf("%X", num);

        //%o는 8진수
        //%x 는 16진수로 표현
        //%X 는 16진수 (대문자)로 표현
    }
}
